# mindcap
star = eval(input("Free stars!!! input a number"))
if star <= 10 and star >= 3 :

    for uwu in range(star) :
        for owo in range(uwu + 1) :
            print(" ", end= " ")
        for owo in range(uwu, star) :
            print("* ", end= "")
        print()
else:
    print("No more than 10 and less than 3!")
    